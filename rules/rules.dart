/*
Em um jogo de Dungeons and Dragons, os personagens geralmente têm atributos que determinam suas habilidades e características. Alguns dos atributos mais comuns incluem:

    Força: determina a força física do personagem e sua capacidade de usar armas e equipamentos pesados.

    Destreza: determina a agilidade do personagem e sua habilidade em tarefas que exigem habilidades manuais precisas, como atirar com uma arma ou desarmar uma armadilha.

    Constituição: determina a saúde e resistência do personagem e sua capacidade de resistir a doenças e ferimentos.

    Inteligência: determina a inteligência e a capacidade de aprendizado do personagem e sua habilidade em tarefas que exigem raciocínio lógico ou conhecimento especializado.

    Sabedoria: determina a percepção e intuição do personagem e sua habilidade em tarefas que exigem sabedoria e discernimento.

    Carisma: determina a personalidade e a habilidade de persuasão do personagem e sua habilidade em se relacionar com outras criaturas.

Esses são apenas alguns exemplos de atributos que um personagem de Dungeons and Dragons pode ter. Cada jogo de Dungeons and Dragons pode ter uma lista de atributos ligeiramente diferente, dependendo da versão do jogo e do sistema de regras que está sendo usado.

